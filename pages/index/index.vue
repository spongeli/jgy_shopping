<script>
	import goodsList from '../../components/common/goods-list.vue';
	import headSwiper from '../../components/index/head-swiper.vue';
	import loadMore from '../../components/common/load-more.vue';
	export default {
		components: {
			goodsList,
			headSwiper,
			loadMore
		},
		data() {
			return {
				titleNViewBackground: '',
				swiperCurrent: 0,
				swiperLength: 0,
				moreLoadText: '正在加载更多',
				goodsList: [],
				searchInparam: {
					search: "",
					pageInparam: {
						pageSize: 10,
						pageCurrentPage: 1
					}
				},
				// 导航栏目
				carouselList: [],
				// 活动栏目
				activeList: [],
				// 推荐栏目
				recommendList: []
			};
		},
		onLoad() {
			this.loadData();
		},
		methods: {
			async loadData() {
				// 获取首页配置内容
				this.gainIndexSetUp()
				// 请求商品信息
				this.gainGoodsList()
			},
			//轮播图切换修改背景色
			swiperChange(e) {
				const index = e.detail.current;
				this.swiperCurrent = index;
				this.titleNViewBackground = this.carouselList[index].background;
			},
			//详情页
			navToDetailPage(id) {
				//测试数据没有写id，用title代替
				uni.navigateTo({
					url: `/pages/product/product?id=${id}`
				});
			},
			toSearch() {
				uni.navigateTo({
					url: '/pages/search/search'
				});
			},
			// 活动跳转页面
			toActivePage(dest) {
				console.log('点击了活动页面的:' + dest);
				// uni.navigateTo({
				//     url: dest
				// });
			},
			// 获取首页配置内容
			gainIndexSetUp() {
				this.$get(`/index`).then(res => {
					let fail = res[0];
					let success = res[1]
					if (fail) {
						uni.showToast({
							title: "服务器异常",
							icon: "none"
						})
					} else {
						if (success.data.status == 200) {
							let data = success.data.data
							data.forEach(item => {
								switch (item.type) {
									case 0:
										this.carouselList.push(item)
										break;
									case 1:
										this.recommendList.push(item)
										break;
									case 2:
										this.activeList.push(item)
										break;
								}
							})
						}
					}
				})
			},
			// 获取商品信息
			gainGoodsList() {
				this.$post(`/goods`, this.searchInparam).then(res => {
					let fail = res[0];
					let success = res[1]
					if (fail) {
						uni.showToast({
							title: "服务器异常",
							icon: "none"
						})
					} else {
						console.log(success);
						if (success.data.status == 200) {
							let data = success.data.data
							this.searchInparam.pageInparam.pageCurrentPage = data.nextPage
							// 是否还有下一页
							if (!data.hasNextPage) this.moreLoadText = '我是有底线的(⊙o⊙)'
							for (var i in data.list) {
								this.goodsList.push(data.list[i]);
							}
						}
					}
				})
			}
		},
		// #ifndef MP
		// 标题栏input搜索框点击
		onNavigationBarSearchInputClicked: async function(e) {
			uni.navigateTo({
				url: '/pages/search/search'
			});
		},
		//点击导航栏 buttons 时触发
		onNavigationBarButtonTap(e) {
			// #ifdef APP-PLUS
			const pages = getCurrentPages();
			const page = pages[pages.length - 1];
			const currentWebview = page.$getAppWebview();
			currentWebview.hideTitleNViewButtonRedDot({
				index
			});
			// #endif
			uni.navigateTo({
				url: '/pages/notice/notice'
			});
		},
		// #endif
		// 下拉加载更多
		onReachBottom() {
			if (this.moreLoadText == '正在加载更多') {
				this.gainGoodsList();
			}
		}
	};
</script>

